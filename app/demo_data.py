def node_color(nid):
    n = NODES[nid]
    if n['st'] == 'FLOODING':
        return '#d64545'
    if n['st'] == 'HIGH':
        return '#e8a33d'
    if n['type'] == 'OUTFALL':
        return '#5b6772'
    return '#3f9d52'


def node_radius(nid):
    st = NODES[nid]['st']
    return 10 if st == 'FLOODING' else 9 if st == 'HIGH' else 8


def peak_flow(nid):
    return max(NODES[nid]['flo'])


def max_depth(nid):
    return max(NODES[nid]['dep'])


def flood_step_count(nid):
    return sum(1 for v in NODES[nid]['fld'] if v > 0)


def peak_flow_idx(nid):
    flo = NODES[nid]['flo']
    return flo.index(max(flo))
